def main() -> None:
    EVAL_DIR.mkdir(parents=True, exist_ok=True)
    REPORT_DIR.mkdir(parents=True, exist_ok=True)

    locked_paths = {
        "data/features/train_features.parquet": ROOT / "data" / "features" / "train_features.parquet",
        "data/interim/bangladesh_smartgrid_clean.parquet": ROOT / "data" / "interim" / "bangladesh_smartgrid_clean.parquet",
        "graphs/adjacency_matrix.csv": ROOT / "graphs" / "adjacency_matrix.csv",
        "experiments/evaluation_protocol.md": ROOT / "experiments" / "evaluation_protocol.md",
        "ablation/ablation_plan.md": ROOT / "ablation" / "ablation_plan.md",
        "error_analysis/error_taxonomy.md": ROOT / "error_analysis" / "error_taxonomy.md",
    }
    locked_md5 = {k: file_md5(v) for k, v in locked_paths.items() if v.exists()}

    bench = benchmark_registry()
    abl = ablation_registry()

    write_evaluation_protocol(bench, abl)
    write_benchmark_comparison_plan(bench)
    write_robustness_evaluation_plan()
    write_statistical_testing_plan()
    write_figure_and_table_plan(bench, abl)
    write_summary(bench, abl, locked_md5)
    write_decision_report(bench)

    print("Phase 15 final evaluation framework design complete.")
    print(f"Benchmarks: {len(bench)} | Ablations: {len(abl)} | Dimensions: D1–D6")
    print(f"Reports -> {EVAL_DIR.relative_to(ROOT)} , {REPORT_DIR.relative_to(ROOT)}")


if __name__ == "__main__":
    main()
